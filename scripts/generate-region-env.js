const fs = require('fs');
const path = require('path');
const dotenv = require('dotenv');

const root = path.resolve(__dirname, '..');
const rootEnvPath = path.join(root, '.env');

const REGION_TARGETS = {
  ZW: { file: '.env.zw', database: 'fieldcore_zw', port: '3000', seedRegions: 'ZW' },
  SA: { file: '.env.sa', database: 'fieldcore_sa', port: '3001', seedRegions: 'SA' }
};

function readEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return { text: '', parsed: {} };
  const text = fs.readFileSync(filePath, 'utf8');
  return { text, parsed: dotenv.parse(text) };
}

function parseDatabaseUrl(value) {
  try {
    const url = new URL(value);
    if (!url.protocol.startsWith('postgres')) throw new Error('DATABASE_URL must use PostgreSQL.');
    return url;
  } catch (error) {
    throw new Error('Root .env DATABASE_URL is not a valid PostgreSQL URL.');
  }
}

function hasPlaceholderCredentials(value) {
  if (!value) return false;
  try {
    const url = new URL(value);
    return url.username === 'USER' || url.password === 'PASSWORD' || /postgresql:\/\/USER(?::PASSWORD)?@/i.test(value);
  } catch {
    return /postgresql:\/\/USER(?::PASSWORD)?@/i.test(value);
  }
}

function quote(value) {
  if (/^[0-9]+$/.test(String(value))) return String(value);
  return JSON.stringify(String(value));
}

function databaseUrlFor(baseUrl, databaseName) {
  const next = new URL(baseUrl.toString());
  next.pathname = `/${databaseName}`;
  return next.toString();
}

function setEnvValue(text, key, value) {
  const rendered = `${key}=${quote(value)}`;
  const lines = text ? text.replace(/\s*$/, '').split(/\r?\n/) : [];
  const index = lines.findIndex((line) => new RegExp(`^\\s*${key}\\s*=`).test(line));
  if (index >= 0) lines[index] = rendered;
  else lines.push(rendered);
  return `${lines.join('\n')}\n`;
}

function mergeRegionEnv(target, rootParsed, targetText) {
  const values = {
    NODE_ENV: targetText.includes('NODE_ENV=') ? null : rootParsed.NODE_ENV || 'development',
    JWT_SECRET: targetText.includes('JWT_SECRET=') ? null : rootParsed.JWT_SECRET || 'dev-only-change-me-but-make-this-longer-than-32-characters',
    DEMO_PASSWORD: targetText.includes('DEMO_PASSWORD=') ? null : rootParsed.DEMO_PASSWORD || 'FieldCoreDemo2026!',
    SAAS_BILLING_PROVIDER: targetText.includes('SAAS_BILLING_PROVIDER=') ? null : rootParsed.SAAS_BILLING_PROVIDER || 'manual',
    EMAIL_PROVIDER: targetText.includes('EMAIL_PROVIDER=') ? null : rootParsed.EMAIL_PROVIDER || 'console',
    WHATSAPP_PROVIDER: targetText.includes('WHATSAPP_PROVIDER=') ? null : rootParsed.WHATSAPP_PROVIDER || 'console',
    SMS_PROVIDER: targetText.includes('SMS_PROVIDER=') ? null : rootParsed.SMS_PROVIDER || 'console',
    DATABASE_URL: databaseUrlFor(parseDatabaseUrl(rootParsed.DATABASE_URL), target.database),
    FIELDCORE_REGION: target.seedRegions,
    FIELDCORE_SEED_REGIONS: target.seedRegions,
    FIELDCORE_SEED_SAMPLE_DATA: 'false',
    PORT: target.port,
    APP_BASE_URL: `http://localhost:${target.port}`
  };

  let text = targetText || `# Generated by npm run env:regions. Safe to update with local-only regional values.\n`;
  for (const [key, value] of Object.entries(values)) {
    if (value !== null) text = setEnvValue(text, key, value);
  }
  return text;
}

function main() {
  const rootEnv = readEnvFile(rootEnvPath);
  if (!rootEnv.parsed.DATABASE_URL) {
    console.error('Root .env DATABASE_URL is missing. Add your working local PostgreSQL URL to .env first.');
    process.exit(1);
  }
  if (hasPlaceholderCredentials(rootEnv.parsed.DATABASE_URL)) {
    console.error('Root .env DATABASE_URL still contains placeholder database credentials. Fix .env first.');
    process.exit(1);
  }

  parseDatabaseUrl(rootEnv.parsed.DATABASE_URL);

  for (const target of Object.values(REGION_TARGETS)) {
    const targetPath = path.join(root, target.file);
    const current = readEnvFile(targetPath);
    const next = mergeRegionEnv(target, rootEnv.parsed, current.text);
    fs.writeFileSync(targetPath, next);
    console.log(`Updated ${target.file} for ${target.seedRegions} on port ${target.port}.`);
  }

  console.log('Regional env files generated. Database passwords were not printed.');
}

main();
